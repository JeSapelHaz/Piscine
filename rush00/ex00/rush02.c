#include <unistd.h>

void ft_putchar(char c);

void ft_display(int n, char c, int r)
{
	int	i;

	i = 0;
	while (i < n)
	{
		ft_putchar(c);
		i++;
	}
	if (r == 1)
		ft_putchar('\n');
}
